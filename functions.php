<?php


// a function 
function a($value, $link){
    echo '<a href="' . $link . '" ">' . $value . '</a>';
}


// h1 function 
function h1($value){
    echo '<h1>'. $value . '</h1>';
};


// h2 function 
function h2($value){
    echo '<h2>'. $value . '</h2>';
};



// h3 function 
function h3($value){
    echo '<h3>'. $value . '</h3>';
};



// p function 
function p($value){
    echo '<p>'. $value . '</p>';
};



// img function 
function img($value){
    echo '<img src = " ' . $value .' " alt="Photo">' ;
};